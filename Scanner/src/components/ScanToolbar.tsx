const TOOLS: { id: ToolId; label: string; Icon: () => React.JSX.Element }[] = [
  { id: "scan-color", label: "Scan color", Icon: IconScanColor },
  { id: "feedback", label: "Feedback", Icon: IconFeedback },
  { id: "edit", label: "Edit", Icon: IconEdit },
  { id: "swap", label: "Swap", Icon: IconSwap },
];

export default function ScanToolbar({ className, expanded: controlledExpanded, onExpandedChange, onToolClick }: ScanToolbarProps) {
  const [internalExpanded, setInternalExpanded] = useState(false);
  const expanded = controlledExpanded ?? internalExpanded;
  const [activeTools, setActiveTools] = useState<Set<ToolId>>(new Set());

  function toggleExpanded() {
    const next = !expanded;
    onExpandedChange?.(next);
    setInternalExpanded(next);
  }

  return (
    <div
      className={className ?? ""}
      style={{
        padding: 8,
        borderRadius: 8,
        backgroundColor: "#ffffff",
        boxShadow: "0px 4px 12px rgba(0,0,0,0.08)",
      }}
    >
      <div
        className="flex items-stretch justify-center"
        style={{ gap: 8 }}
      >
        <div
          className="flex items-start"
          style={{ gap: 12 }}
        >
          {TOOLS.map((tool) => {
            const isActive = activeTools.has(tool.id);
            return (
              <button
                key={tool.id}
                type="button"
                onClick={() => {
                  setActiveTools((prev) => {
                    const next = new Set(prev);
                    if (next.has(tool.id)) next.delete(tool.id);
                    else next.add(tool.id);
                    return next;
                  });
                  onToolClick?.(tool.id, !isActive);
                }}
                className="flex flex-col items-center justify-center cursor-pointer border-0 appearance-none outline-none transition-ui focus-visible:ring-2 focus-visible:ring-[var(--color-border-focus)] focus-visible:ring-offset-2"
                style={{
                  width: expanded ? 96.67 : 60,
                  height: expanded ? 90 : 60,
                  padding: 0,
                  gap: expanded ? 4 : 0,
                  backgroundColor: "transparent",
                }}
                aria-label={tool.label}
                aria-pressed={isActive}
              >
                <span
                  style={{
                    width: 60,
                    height: 60,
                    borderRadius: 8,
                    display: "flex",
                    alignItems: "center",
                    justifyContent: "center",
                    backgroundColor: isActive ? "#A6E2F9" : "white",
                  }}
                >
                  <tool.Icon />
                </span>
                {expanded && (
                  <span
                    className="tp-body-01"
                    style={{
                      color: isActive
                        ? "var(--color-border-interactive)"
                        : "var(--color-text-primary)",
                      whiteSpace: "nowrap",
                    }}
                  >
                    {tool.label}
                  </span>
                )}
              </button>
            );
          })}
        </div>

        <button
          type="button"
          onClick={toggleExpanded}
          className="flex items-center justify-center cursor-pointer border-0 appearance-none outline-none transition-ui focus-visible:ring-2 focus-visible:ring-[var(--color-border-focus)] focus-visible:ring-offset-2"
          style={{
            width: 60,
            backgroundColor: "white",
            padding: 0,
            flexShrink: 0,
          }}
          aria-label={expanded ? "Collapse toolbar" : "Expand toolbar"}
          aria-expanded={expanded}
        >
          <ChevronIcon up={expanded} />
        </button>
      </div>
    </div>
  );
}
